#include "EKF.h"

void kalman_1(struct _1_ekf_filter *ekf, float input){
    // ============== 预测步骤（Prediction Step）==============
    // 第1步：预测误差协方差更新
    // 公式：P_k|k-1 = P_k-1|k-1 + Q
    // 含义：上一时刻的估计不确定性 + 系统过程噪声 = 当前时刻的预测不确定性
    // Q值越大表示系统越不稳定，预测不确定性增加越快
    ekf->Now_P = ekf->LastP + ekf->Q;
    
    // ============== 更新步骤（Update Step）==============
    // 第2步：计算卡尔曼增益
    // 公式：K = P_k|k-1 / (P_k|k-1 + R)
    // 含义：卡尔曼增益决定了对预测值和测量值的信任程度
    // - 当R<<P时，Kg接近1，更信任测量值（传感器噪声小）
    // - 当R>>P时，Kg接近0，更信任预测值（传感器噪声大）
    // - 这是卡尔曼滤波器自适应的核心：动态平衡预测和测量的权重
    ekf->Kg = ekf->Now_P / (ekf->Now_P + ekf->R);
    
    // 第3步：状态估计更新（最优融合）
    // 公式：x_k|k = x_k|k-1 + K * (z_k - x_k|k-1)
    // 含义：新的最优估计 = 预测值 + 卡尔曼增益 × (测量值 - 预测值)
    // - (input - ekf->out) 是测量残差，表示测量值与预测值的差异
    // - 卡尔曼增益Kg控制这个残差对最终估计的贡献程度
    // - 这样既利用了系统的动态特性（预测），又考虑了实际测量值
    ekf->out = ekf->out + ekf->Kg * (input - ekf->out);
    
    // 第4步：误差协方差更新
    // 公式：P_k|k = (1 - K) * P_k|k-1
    // 含义：更新后的估计不确定性总是小于预测不确定性
    // - 这体现了卡尔曼滤波器的核心思想：融合信息总能减少不确定性
    // - (1 - Kg)确保协方差矩阵保持正定性，算法数值稳定
    // - 该值将作为下一次滤波的LastP，形成递归结构
    ekf->LastP = (1 - ekf->Kg) * ekf->Now_P;
    
    // 总结：经过一次卡尔曼滤波循环后：
    // 1. 获得了当前时刻的最优状态估计（ekf->out）
    // 2. 更新了估计不确定性（ekf->LastP），为下次滤波做准备
    // 3. 在数学上保证了估计的最优性（最小均方误差意义下）
}
